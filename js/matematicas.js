
/*
La funcion suma tiene 2 parametros de entrada y uno de salida con el 
resultado
*/
function sumar(a,b){
  var resultado = a + b;
  return resultado;
}

function resta(a,b){
  var resultado = a - b;
  return resultado;
}

function multiplicacion(a,b){
  var resultado = a * b;
  return resultado;
}

function division(a,b){
  var resultado = a / b;
  return resultado;
}